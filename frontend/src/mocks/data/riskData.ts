import type {
  RiskCase,
  RiskLevel,
  RiskManagementData,
} from "../types/risk";

function scoreLevel(score: number): RiskLevel {
  if (score >= 20) return "Critical";
  if (score >= 12) return "High";
  if (score >= 6) return "Medium";
  return "Low";
}

function assessment(
  likelihood: number,
  impact: number,
  controlEffectivenessPercent: number,
  residualLikelihood: number,
  residualImpact: number,
  treatment: RiskCase["assessment"]["treatmentDecision"],
  acceptance: RiskCase["assessment"]["acceptanceStatus"]
): RiskCase["assessment"] {
  const inherentScore = likelihood * impact;
  const residualScore = residualLikelihood * residualImpact;
  return {
    likelihood,
    impact,
    inherentScore,
    inherentLevel: scoreLevel(inherentScore),
    controlEffectivenessPercent,
    residualLikelihood,
    residualImpact,
    residualScore,
    residualLevel: scoreLevel(residualScore),
    treatmentDecision: treatment,
    acceptanceStatus: acceptance,
    methodologyNote:
      "Risk-scoring methodology is configurable and will later follow the organization’s approved framework.",
  };
}

