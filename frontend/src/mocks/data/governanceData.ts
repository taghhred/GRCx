function withStatus(
  base: Omit<GovernanceKpi, "status"> & { status?: GovernanceKpi["status"] }
): GovernanceKpi {
  const status = calculateKpiStatus({
    value: base.currentValue,
    target: base.target,
    warningThreshold: base.warningThreshold,
    criticalThreshold: base.criticalThreshold,
    direction: base.direction,
    targetMin: base.targetMin,
    targetMax: base.targetMax,
  });
  return { ...base, status };
}

