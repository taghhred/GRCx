export function nextCaseId(existing: SoarGrcCase[]): string {
  const nums = existing.map((c) => Number(c.caseId.replace(/\D/g, "")) || 0);
  const next = Math.max(2500, ...nums) + 1;
  return `GRC-${next}`;
}

