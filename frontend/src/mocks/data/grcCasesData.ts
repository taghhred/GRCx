function ev(
  id: string,
  type: CaseEvidenceItem["type"],
  name: string,
  detail: string,
  addedAt: string,
  extra?: Partial<CaseEvidenceItem>
): CaseEvidenceItem {
  return { id, type, name, detail, addedAt, ...extra };
}

